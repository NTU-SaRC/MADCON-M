function q = AU2Q( angle, u )

%-------------------------------------------------------------------------------
%   Convert an angle and a unit vector to a quaternion.
%-------------------------------------------------------------------------------
%   Form:
%   q = AU2Q( angle, u )
%-------------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   angle         (1,:)   Angle
%   u             (3,1)   Unit vector
%
%   -------
%   Outputs
%   -------
%   q             (4,:)   Quaternion
%
%-------------------------------------------------------------------------------

%-------------------------------------------------------------------------------
%	References:	None
%-------------------------------------------------------------------------------
%	 Copyright 1997 Princeton Satellite Systems, Inc. All rights reserved.
%-------------------------------------------------------------------------------

% Assume it is a valid unit vector
%---------------------------------
halfAngle = angle/2;
c         = cos( halfAngle );
s         = sin( halfAngle );

q         = [ c; -[s*u(1);s*u(2);s*u(3)] ];


