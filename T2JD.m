function jd = T2JD( T )
	
%-------------------------------------------------------------------------------
%   Converts Julian centuries from J2000.0 to days
%-------------------------------------------------------------------------------
%   Form:
%   jd = T2JD( T )
%-------------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   T      (1,:)  Julian centuries of 86400s dynamical time from j2000.0
%
%   -------
%   Outputs
%   -------
%   jd     (1,:)  Julian date (days)
%
%-------------------------------------------------------------------------------

%-------------------------------------------------------------------------------
%   Copyright (c) 1993 Princeton Satellite Systems, Inc.
%   All rights reserved.
%-------------------------------------------------------------------------------

jd = T*36525 + 2451545;

