function T = JD2T( jd )
	
%-------------------------------------------------------------------------------
%   Converts Julian days to centuries from J2000.0
%-------------------------------------------------------------------------------
%   Form:
%   T = JD2T( jd )
%-------------------------------------------------------------------------------
%
%   ------
%   Inputs
%   ------
%   jd           Julian date (days)
%
%   -------
%   Outputs
%   -------
%   T            Julian centuries of 86400s dynamical time from j2000.0
%
%-------------------------------------------------------------------------------

%-------------------------------------------------------------------------------
%   Copyright (c) 1993 Princeton Satellite Systems, Inc.
%   All rights reserved.
%-------------------------------------------------------------------------------

if( nargin < 1 )
  jd = Date2JD;
end

T = (jd - 2451545) / 36525;

